T0=[0.6,0.15,0.15,0.15];%采样间隔
N0=[256,256,256,2048];%序列长度初始值
for r=1:4%根据T和N的值得出n的值
 	T=T0(r);N=N0(r);n=0:N-1;%求频率分辨率
    D=2*pi/(N*T);
    x=exp(-0.01*n*T).*cos(2*n*T)+2*exp(-0.015*n*T).*cos(2.1*n*T); 
    Xa=T*fftshift(fft(x));
    [r,Xa(1)]%输出变量r和Xa的第一个元素的值
    k=floor(-(N-1)/2:(N-1)/2);%求出图的对称与0频率的傅里叶变换的位置向量
    subplot(2,2,r),plot(k*D,abs(Xa))%画出k*D与傅里叶序列值的图像
end %得出四个数据的频谱特性，每个图像表示一个数据组
